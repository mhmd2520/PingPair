def _segment_perf_table(cases: list[CaseMetrics]) -> str:
    rows = [
        [
            c.case_idx,
            c.payload_bytes,
            c.bandwidth_mbps_pushed,
            _fmt(c.throughput_mbps_received, ".2f"),
            _fmt(c.jitter_ms, ".3f"),
            _fmt(c.packet_loss_pct, ".3f"),
            _fmt(c.min_latency_ms, ".2f"),
            _fmt(c.avg_latency_ms, ".2f"),
            _fmt(c.max_latency_ms, ".2f"),
        ]
        for c in cases
    ]
    return tabulate(rows, headers=_HEADERS, tablefmt="github", numalign="right")


def write_multi_txt(
    report: MultiSegmentRunReport,
    dest: Path,
    *,
    include_appendix: bool = False,
) -> None:
    """Plain-text multi-segment report. Per-case forensics omitted —
    available in the .json sidecar instead. Matches the docx
    writer's pragmatism: prioritise the comparison view."""
    lines: list[str] = []
    sep = "=" * 80
    lines.append(sep)
    lines.append(
        "  PingPair — LAN Characterization Report (Multi-Segment)".center(80)
    )
    lines.append(sep)
    lines.append("")
    info = [
        f"Run ID:          {report.run_id}",
        f"Started:         {report.started_at.strftime('%Y-%m-%d %H:%M:%S')}",
        f"Finished:        {report.ended_at.strftime('%Y-%m-%d %H:%M:%S')}",
        f"Total duration:  {fmt_duration(report.duration_s)}",
        f"Server IP:       {report.server_ip}",
        f"Client IP:       {report.client_ip}",
        f"Protocol:        {report.protocol.upper()}",
        f"Segments ok:     {report.segments_ok} / {report.segments_total}",
        f"Total cases ok:  {report.total_cases_ok} / {report.total_cases}",
        f"fping:           {report.fping_version}",
        f"iperf3:          {report.iperf3_version}",
        f"PingPair:       {report.app_version}",
    ]
    for label, value in metadata_rows(report):
        info.append(f"{label}: {value}")
    lines.extend(info)
    lines.append("")

    # Segments summary table.
    lines.append("-" * 80)
    lines.append("Segments summary")
    lines.append("-" * 80)
    lines.append(tabulate(
        segment_summary_rows(report),
        headers=list(SEGMENT_SUMMARY_HEADERS),
        tablefmt="github",
    ))
    lines.append("")

    # Per-segment Performance Metrics.
    for seg in report.segments:
        lines.append("-" * 80)
        title_line = (
            f"Segment #{seg.segment_idx} — {seg.label} "
            f"({fmt_duration(seg.duration_s)}, {seg.status.upper()})"
        )
        lines.append(title_line)
        if seg.error:
            lines.append(f"Note: {seg.error}")
        lines.append("-" * 80)
        lines.append(_segment_perf_table(seg.cases))
        lines.append("")

    # Cross-segment comparison — one table per metric.
    for metric_id, metric_label in (
        ("throughput", "Cross-segment comparison: Throughput Received (Mbps)"),
        ("avg_latency", "Cross-segment comparison: Average Latency (ms)"),
        ("loss", "Cross-segment comparison: Packet Loss (%)"),
    ):
        headers, rows = cross_segment_comparison(report, metric=metric_id)
        lines.append("-" * 80)
        lines.append(metric_label)
        lines.append("-" * 80)
        lines.append(tabulate(rows, headers=headers, tablefmt="github"))
        lines.append("")

    lines.append(sep)
    lines.append(f"Generated by PingPair {report.app_version}".center(80))
    lines.append(sep)

    if include_appendix:
        from .appendix import append_to_txt
        append_to_txt(lines, report, is_multi=True)

    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_text("\n".join(lines), encoding="utf-8")
